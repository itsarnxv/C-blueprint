
/*

File Modes (r, w, a)
File Modes in C

Mode	   Description
"r"	 -->   Open for reading (file must exist)
"w"	 -->   Open for writing (creates a new file or overwrites existing)
"a"	 -->   Open for appending (adds to the end of file)
"r+" -->   Open for reading and writing
"w+" -->   Create for reading and writing (overwrites existing)
"a+" -->   Open for reading and appending
"wb" -->   Open for file for writing in binary
"ab" -->   Open a file foe appending in binary

*/